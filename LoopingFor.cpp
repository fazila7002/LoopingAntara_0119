#include <iostream>
using namespace std;

int main() {
    //ASCENDING
    cout << "PERULANGAN FOR " << endl;
    for (int i= 0 ; i < 5 ; i++ ) 
    {
        cout << "Nilai i: " << i << " Trknologi Informasi UMY" << endl;
    }
    cout << endl;


    //DESCENDING
    cout << "PERULANGAN FOR TURUN " << endl;
    for (int i= 5 ; i > 0 ; i-- ) 
    {
        cout << "Nilai i: " << i << " Trknologi Informasi UMY" << endl;
    }
    cout << endl;
    
    return 0;
}